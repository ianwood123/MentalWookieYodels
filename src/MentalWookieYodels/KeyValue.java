package MentalWookieYodels;

/**
 *
 * @author woodi
 */
public class KeyValue {
    
    public String key;
    public String value;
        
    public KeyValue(String k, String v){
        this.key = k;
        this.value = v;
    }

    KeyValue() {
        this.key = "";
        this.value = "";
    }
}
